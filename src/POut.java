public class POut {
	public static void beginTableRow(){
		System.out.print("|\t");
	}
	
	public static void separatorTableRow(){
		System.out.print("\t|\t");
	}
	
	public static void endTableRow(){
		System.out.print("\t|\n");
	}
	
	public static void space(){
		System.out.print(" ");
	}
	public static void newLine(){
		System.out.print("\n");
	}
	
	

}
